public class DataSM {

    private String tgl, no, no_surat, tgl_surat, dari, isi, lampiran, ket;

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getTgl() {
        return tgl;
    }

    public void setTgl(String tgl) {
        this.tgl = tgl;
    }

    public String getNo_Surat() {
        return no_surat;
    }

    public void setNo_Surat(String no_surat) {
        this.no_surat = no_surat;
    }

    public String getTgl_Surat() {
        return tgl_surat;
    }

    public void setTgl_Surat(String tgl_surat) {
        this.tgl_surat = tgl_surat;
    }

    public String getDari() {
        return dari;
    }

    public void setDari(String dari) {
        this.dari = dari;
    }

    public String getIsi() {
        return isi;
    }

    public void setIsi(String isi) {
        this.isi = isi;
    }

    public String getLampiran() {
        return lampiran;
    }

    public void setLampiran(String lampiran) {
        this.lampiran = lampiran;
    }

    public String getKet() {
        return ket;
    }

    public void setKet(String ket) {
        this.ket = ket;
    }
}